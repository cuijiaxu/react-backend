package com.cic.its.web.datamodel.dashboard;

public class Sales {
	
	private int name;
	private int Clothes;
	private int Food;
	private int Electronics;
	public int getName() {
		return name;
	}
	public void setName(int name) {
		this.name = name;
	}
	public int getClothes() {
		return Clothes;
	}
	public void setClothes(int clothes) {
		Clothes = clothes;
	}
	public int getFood() {
		return Food;
	}
	public void setFood(int food) {
		Food = food;
	}
	public int getElectronics() {
		return Electronics;
	}
	public void setElectronics(int electronics) {
		Electronics = electronics;
	}
	
	

}
