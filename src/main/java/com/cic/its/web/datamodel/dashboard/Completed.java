package com.cic.its.web.datamodel.dashboard;

public class Completed {

	private int name;
	private int TaskComplete;
	private int CardsComplete;
	public int getName() {
		return name;
	}
	public void setName(int name) {
		this.name = name;
	}
	public int getTaskComplete() {
		return TaskComplete;
	}
	public void setTaskComplete(int taskComplete) {
		TaskComplete = taskComplete;
	}
	public int getCardsComplete() {
		return CardsComplete;
	}
	public void setCardsComplete(int cardsComplete) {
		CardsComplete = cardsComplete;
	}
	
	
}
